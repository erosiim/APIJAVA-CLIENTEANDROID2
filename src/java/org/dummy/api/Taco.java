package org.dummy.api;

/**
 *
 * @author exkapp
 */
public class Taco {
    private String id_taco;
    private String nombre;
    private String precio;

    public String getId_taco() {
        return id_taco;
    }

    public void setId_taco(String id_taco) {
        this.id_taco = id_taco;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPrecio() {
        return precio;
    }

    public void setPrecio(String precio) {
        this.precio = precio;
    }
    
    
}
